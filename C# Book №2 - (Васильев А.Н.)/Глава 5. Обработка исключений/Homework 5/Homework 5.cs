/*Напишите программу, содержащую статический метод, первым ар-
гументом которого является целочисленный массив, а вторым аргу-
ментом — целочисленное значение. Методом при вызове выполняется
проверка относительно наличия в массиве (первый аргумент) элемен-
та со значением, определяемым вторым аргументом. Если такой эле-
мент есть, то результатом возвращается индекс этого элемента (первого
из них, если таких элементов несколько). Если элемента с указанным
значением в массиве нет, то метод генерирует исключение. Предложите
способ проверки функциональности метода, включая обработку исклю-
чений.*/

using System;

class Program
{
    // Статический метод для поиска элемента в массиве
    public static int FindElementIndex(int[] array, int value)
    {
        for (int i = 0; i < array.Length; i++)
        {
            if (array[i] == value)
            {
                return i; // Возвращаем индекс первого найденного элемента
            }
        }

        // Если элемент не найден — выбрасываем исключение
        throw new ArgumentException($"Элемент со значением {value} не найден в массиве.");
    }

    static void Main(string[] args)
    {
        int[] numbers = { 10, 20, 30, 40, 50 };

        // Пример с элементом, который есть в массиве
        try
        {
            int index = FindElementIndex(numbers, 30);
            Console.WriteLine($"Элемент найден на индексе: {index}");
        }
        catch (ArgumentException ex)
        {
            Console.WriteLine($"Ошибка: {ex.Message}");
        }

        // Пример с элементом, которого нет в массиве
        try
        {
            int index = FindElementIndex(numbers, 99);
            Console.WriteLine($"Элемент найден на индексе: {index}");
        }
        catch (ArgumentException ex)
        {
            Console.WriteLine($"Ошибка: {ex.Message}");
        }
    }
}
